"use client";

import { useState, useEffect } from "react";
import { Link, useNavigate } from "react-router-dom";
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";
import {
  faCheckCircle,
  faExclamationTriangle,
  faArrowLeft,
  faRedo,
  faDownload,
  faHeartbeat,
  faAppleAlt,
  faRunning,
  faUserMd,
} from "@fortawesome/free-solid-svg-icons";

const Results = () => {
  const navigate = useNavigate();
  const [result, setResult] = useState(null);

  useEffect(() => {
    const storedResult = sessionStorage.getItem("predictionResult");
    if (storedResult) {
      setResult(JSON.parse(storedResult));
    } else {
      navigate("/prediction");
    }
  }, [navigate]);

  if (!result) {
    return (
      <div className="min-h-screen bg-gray-50 flex items-center justify-center">
        <div className="text-center">
          <FontAwesomeIcon
            icon={faHeartbeat}
            className="h-12 w-12 text-blue-600 animate-pulse mb-4"
          />
          <p className="text-gray-600">Memuat hasil...</p>
        </div>
      </div>
    );
  }

  const isHighRisk = result.prediction === 1 || result.probability > 0.5;
  const riskPercentage = Math.round(result.probability * 100);

  const recommendations = isHighRisk
    ? [
        {
          icon: faUserMd,
          title: "Konsultasi Dokter",
          description:
            "Segera konsultasikan dengan dokter untuk pemeriksaan lebih lanjut",
        },
        {
          icon: faAppleAlt,
          title: "Pola Makan Sehat",
          description:
            "Kurangi konsumsi gula dan karbohidrat, perbanyak sayuran dan protein",
        },
        {
          icon: faRunning,
          title: "Olahraga Teratur",
          description: "Lakukan aktivitas fisik minimal 30 menit setiap hari",
        },
        {
          icon: faHeartbeat,
          title: "Monitor Kesehatan",
          description: "Pantau kadar gula darah dan tekanan darah secara rutin",
        },
      ]
    : [
        {
          icon: faHeartbeat,
          title: "Pertahankan Gaya Hidup Sehat",
          description: "Terus jaga pola makan dan olahraga yang sudah baik",
        },
        {
          icon: faAppleAlt,
          title: "Pola Makan Seimbang",
          description: "Pertahankan diet seimbang dengan porsi yang tepat",
        },
        {
          icon: faRunning,
          title: "Aktivitas Fisik",
          description: "Lanjutkan rutinitas olahraga untuk menjaga kesehatan",
        },
        {
          icon: faUserMd,
          title: "Pemeriksaan Berkala",
          description: "Lakukan check-up kesehatan secara berkala",
        },
      ];

  const generateReport = () => {
    const reportData = {
      timestamp: new Date().toLocaleString("id-ID"),
      inputData: result.inputData,
      prediction: result.prediction,
      probability: result.probability,
      riskLevel: isHighRisk ? "Tinggi" : "Rendah",
    };

    const reportText = `
LAPORAN PREDIKSI RISIKO DIABETES
================================

Tanggal: ${reportData.timestamp}

DATA INPUT:
- Usia: ${result.inputData.age} tahun
- Kadar Glukosa: ${result.inputData.glucose} mg/dL
- Tekanan Darah: ${result.inputData.bloodPressure} mmHg
- BMI: ${result.inputData.bmi}
- Ketebalan Kulit: ${result.inputData.skinThickness || "Tidak diisi"} mm
- Insulin: ${result.inputData.insulin || "Tidak diisi"} mu U/ml
- Riwayat Keluarga: ${result.inputData.diabetesPedigreeFunction || "Tidak diisi"}
- Jumlah Kehamilan: ${result.inputData.pregnancies || "Tidak diisi"}

HASIL PREDIKSI:
- Tingkat Risiko: ${reportData.riskLevel}
- Probabilitas: ${riskPercentage}%

DISCLAIMER:
Hasil ini hanya untuk referensi dan tidak menggantikan diagnosis medis profesional.
Konsultasikan dengan dokter untuk pemeriksaan lebih lanjut.

Generated by DiabeaCheck - Sistem Prediksi Diabetes
    `;

    const blob = new Blob([reportText], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `laporan-diabetes-${Date.now()}.txt`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };

  return (
    <div className="min-h-screen bg-gray-50 py-12">
      <div className="max-w-4xl mx-auto px-4 sm:px-6 lg:px-8">
        {/* Header */}
        <div className="text-center mb-8">
          <h1 className="text-4xl font-bold text-gray-900 mb-4">
            Hasil Prediksi Diabetes
          </h1>
          <p className="text-xl text-gray-600">
            Berikut adalah hasil analisis risiko diabetes Anda
          </p>
        </div>

        {/* Main Result Card */}
        <div
          className={`bg-white rounded-lg shadow-lg p-8 mb-8 border-l-4 ${
            isHighRisk ? "border-red-500" : "border-green-500"
          }`}
        >
          <div className="flex items-center justify-between mb-6">
            <div className="flex items-center">
              <FontAwesomeIcon
                icon={isHighRisk ? faExclamationTriangle : faCheckCircle}
                className={`h-12 w-12 mr-4 ${isHighRisk ? "text-red-500" : "text-green-500"}`}
              />
              <div>
                <h2 className="text-2xl font-bold text-gray-900">
                  Risiko {isHighRisk ? "Tinggi" : "Rendah"}
                </h2>
                <p className="text-gray-600">Probabilitas: {riskPercentage}%</p>
              </div>
            </div>
            <div className="text-right">
              <div
                className={`text-4xl font-bold ${isHighRisk ? "text-red-500" : "text-green-500"}`}
              >
                {riskPercentage}%
              </div>
              <div className="text-sm text-gray-500">Tingkat Risiko</div>
            </div>
          </div>

          <div
            className={`p-4 rounded-lg ${isHighRisk ? "bg-red-50" : "bg-green-50"}`}
          >
            <p className={`${isHighRisk ? "text-red-800" : "text-green-800"}`}>
              {isHighRisk
                ? "Hasil menunjukkan risiko diabetes yang tinggi. Disarankan untuk segera berkonsultasi dengan dokter untuk pemeriksaan lebih lanjut."
                : "Hasil menunjukkan risiko diabetes yang rendah. Tetap jaga pola hidup sehat untuk mempertahankan kondisi ini."}
            </p>
          </div>
        </div>

        {/* Input Data Summary */}
        <div className="bg-white rounded-lg shadow-lg p-8 mb-8">
          <h3 className="text-xl font-bold text-gray-900 mb-4">
            Ringkasan Data Input
          </h3>
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-4">
            <div className="bg-gray-50 p-4 rounded-lg">
              <div className="text-sm text-gray-600">Usia</div>
              <div className="text-lg font-semibold">
                {result.inputData.age} tahun
              </div>
            </div>
            <div className="bg-gray-50 p-4 rounded-lg">
              <div className="text-sm text-gray-600">Glukosa</div>
              <div className="text-lg font-semibold">
                {result.inputData.glucose} mg/dL
              </div>
            </div>
            <div className="bg-gray-50 p-4 rounded-lg">
              <div className="text-sm text-gray-600">Tekanan Darah</div>
              <div className="text-lg font-semibold">
                {result.inputData.bloodPressure} mmHg
              </div>
            </div>
            <div className="bg-gray-50 p-4 rounded-lg">
              <div className="text-sm text-gray-600">BMI</div>
              <div className="text-lg font-semibold">
                {result.inputData.bmi}
              </div>
            </div>
          </div>
        </div>

        {/* Recommendations */}
        <div className="bg-white rounded-lg shadow-lg p-8 mb-8">
          <h3 className="text-xl font-bold text-gray-900 mb-6">Rekomendasi</h3>
          <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
            {recommendations.map((rec, index) => (
              <div key={index} className="flex items-start space-x-4">
                <div
                  className={`p-3 rounded-lg ${isHighRisk ? "bg-red-100" : "bg-blue-100"}`}
                >
                  <FontAwesomeIcon
                    icon={rec.icon}
                    className={`h-6 w-6 ${isHighRisk ? "text-red-600" : "text-blue-600"}`}
                  />
                </div>
                <div>
                  <h4 className="font-semibold text-gray-900 mb-1">
                    {rec.title}
                  </h4>
                  <p className="text-gray-600 text-sm">{rec.description}</p>
                </div>
              </div>
            ))}
          </div>
        </div>

        {/* Action Buttons */}
        <div className="flex flex-col sm:flex-row gap-4 justify-center">
          <Link
            to="/prediction"
            className="bg-blue-600 text-white px-6 py-3 rounded-lg font-semibold hover:bg-blue-700 transition-colors flex items-center justify-center"
          >
            <FontAwesomeIcon icon={faRedo} className="mr-2" />
            Prediksi Ulang
          </Link>
          <button
            onClick={generateReport}
            className="bg-green-600 text-white px-6 py-3 rounded-lg font-semibold hover:bg-green-700 transition-colors flex items-center justify-center"
          >
            <FontAwesomeIcon icon={faDownload} className="mr-2" />
            Unduh Laporan
          </button>
          <Link
            to="/"
            className="bg-gray-600 text-white px-6 py-3 rounded-lg font-semibold hover:bg-gray-700 transition-colors flex items-center justify-center"
          >
            <FontAwesomeIcon icon={faArrowLeft} className="mr-2" />
            Kembali ke Beranda
          </Link>
        </div>

        {/* Disclaimer */}
        <div className="mt-8 bg-yellow-50 border border-yellow-200 rounded-lg p-6">
          <h3 className="text-lg font-semibold text-yellow-800 mb-2">
            Disclaimer:
          </h3>
          <p className="text-yellow-700">
            Hasil prediksi ini menggunakan algoritma Machine Learning dan hanya
            untuk referensi. Tidak menggantikan diagnosis medis profesional.
            Selalu konsultasikan dengan dokter untuk pemeriksaan dan diagnosis
            yang akurat.
          </p>
        </div>
      </div>
    </div>
  );
};

export default Results;
